package com.wingplus.coomohome.web;

/**
 * 服务
 * @author leaffun.
 *         Create on 2017/9/27.
 */
public class Service {
    private int img;

    private String name;

    private int type;

    private int num;


    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
